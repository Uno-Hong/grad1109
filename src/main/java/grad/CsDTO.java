package grad;

public class CsDTO {
	private int cs_number;
	private String cs_title;
	private String cs_id;
	private String cs_article;
	private String cs_answer;
	private String cs_date;
	private int cs_state;
	
	public int getCs_number() {
		return cs_number;
	}
	public void setCs_number(int cs_number) {
		this.cs_number = cs_number;
	}
	public String getCs_title() {
		return cs_title;
	}
	public void setCs_title(String cs_title) {
		this.cs_title = cs_title;
	}
	public String getCs_id() {
		return cs_id;
	}
	public void setCs_id(String cs_id) {
		this.cs_id = cs_id;
	}
	public String getCs_article() {
		return cs_article;
	}
	public void setCs_article(String cs_article) {
		this.cs_article = cs_article;
	}
	public String getCs_date() {
		return cs_date;
	}
	public void setCs_date(String cs_date) {
		this.cs_date = cs_date;
	}
	public int getCs_state() {
		return cs_state;
	}
	public void setCs_state(int cs_state) {
		this.cs_state = cs_state;
	}
	public String getCs_answer() {
		return cs_answer;
	}
	public void setCs_answer(String cs_answer) {
		this.cs_answer = cs_answer;
	}
	
}
