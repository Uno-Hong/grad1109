package grad;

public class RestDTO {
	private String rest_name;
	private String rest_addr;
	private String rest_pn;
	private Float rest_lng;
	private Float rest_lat;
	private String rest_etc;
	
	public String getRest_name() {
		return rest_name;
	}
	public void setRest_name(String rest_name) {
		this.rest_name = rest_name;
	}
	public String getRest_addr() {
		return rest_addr;
	}
	public void setRest_addr(String rest_addr) {
		this.rest_addr = rest_addr;
	}
	public String getRest_pn() {
		return rest_pn;
	}
	public void setRest_pn(String rest_pn) {
		this.rest_pn = rest_pn;
	}
	public Float getRest_lng() {
		return rest_lng;
	}
	public void setRest_lng(Float rest_lng) {
		this.rest_lng = rest_lng;
	}
	public Float getRest_lat() {
		return rest_lat;
	}
	public void setRest_lat(Float rest_lat) {
		this.rest_lat = rest_lat;
	}
	public String getRest_etc() {
		return rest_etc;
	}
	public void setRest_etc(String rest_etc) {
		this.rest_etc = rest_etc;
	}
	
	
}
