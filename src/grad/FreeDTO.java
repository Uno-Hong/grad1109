package grad;

public class FreeDTO {
	private int article_index;
	private String article_title;
	private String user_id;
	private String article_detail;
	private int article_hit;
	private int article_good;
	private String article_date;
	private int available;
	
	public int getArticle_index() {
		return article_index;
	}
	public void setArticle_index(int article_index) {
		this.article_index = article_index;
	}
	
	public String getArticle_title() {
		return article_title;
	}
	public void setArticle_title(String article_title) {
		this.article_title = article_title;
	}
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getArticle_detail() {
		return article_detail;
	}
	public void setArticle_detail(String article_detail) {
		this.article_detail = article_detail;
	}
		
	public int getArticle_hit() {
		return article_hit;
	}
	public void setArticle_hit(int article_hit) {
		this.article_hit = article_hit;
	}
	
	public int getArticle_good() {
		return article_good;
	}
	public void setArticle_good(int article_good) {
		this.article_good = article_good;
	}
	
	public String getArticle_date() {
		return article_date;
	}
	public void setArticle_date(String article_date) {
		this.article_date = article_date;
	}
	
	public int getAvailable() {
		return available;
	}
	public void setAvailable(int available) {
		this.available = available;
	}
	
}
